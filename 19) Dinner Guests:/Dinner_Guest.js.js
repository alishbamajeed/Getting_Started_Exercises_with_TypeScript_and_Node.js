"use strict";
// Example TypeScript program with a list of dinner guests
let dinnerGuests = ['Maria', 'Alishba', 'Sakeena',];
// Print the list of dinner guests
console.log("List of dinner guests:");
for (let guest of dinnerGuests) {
    console.log(guest);
}
// Print the number of dinner guests
console.log(`You are inviting ${dinnerGuests.length} people to dinner.`);
